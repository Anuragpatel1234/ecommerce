export const getProductsByCategory = (category) => {
  return sampleProducts.filter(product => 
    product.category.toUpperCase() === category.toUpperCase()
  );
};

export const getProductById = (id) => {
  return sampleProducts.find(product => product._id === id);
};

export const getFeaturedProducts = () => {
  return sampleProducts.filter(product => product.featured);
};

export const getBestsellerProducts = () => {
  return sampleProducts.filter(product => product.bestseller);
};

export const getNewArrivalProducts = () => {
  return sampleProducts.filter(product => product.newArrival);
};

export const getOnSaleProducts = () => {
  return sampleProducts.filter(product => product.onSale);
};